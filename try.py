print('Значение температуры t для определения температурной поправки:')
t = float(input())
print("Введите значение атмосферного давления в Па: ")
Psi = float(input())
a = 24
b = 1.2
c = 0.00186
d = 0.00026
k = 0.000312
Pk = 97592
Pt1 = round(a - (b*t) - (c*(t**2)) + (d*(t**3)) + (k*(t - 20)*(Pk - Psi)), 0)
Pt = round((Pt1/1000), 2)
print(f'Температурная поправка равна Pt= {Pt1}, (Па)')
print(f'Температурная поправка равна Pt= {Pt}, (кПа)')